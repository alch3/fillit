#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	ft_strncpy(&s1[ft_strlen(s1)], s2, n);
	s1[ft_strlen(s1) + n] = '\0';
	return (s1);
}
